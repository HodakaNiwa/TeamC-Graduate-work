void CRanking::Init(void)
{
	//ベースの初期化
	CModeBase::Init();

	//UI情報の読み込み
	LoadUI(LOAD_UI);
}
//=============================================================================
// 終了処理
//=============================================================================
void CRanking::Uninit(void)
{
	//ベースの破棄
	CModeBase::Uninit();

	//フェード以外削除
	CScene::ReleaseFade();
}

//=============================================================================
// 更新処理
//=============================================================================
void CRanking::Update(void)
{
	//サウンドの取得
	CSound *pSound = CManager::GetSound();

	//インプットの取得　
	CGamePad	*pGamePad = CManager::GetInputGamePad();
	CInputKeyboard * pKeyboard = CManager::GetInputkeyboard();

	if (pKeyboard->GetKeyboardTrigger(DIK_RETURN) == true)
	{//ランキングからタイトルへ
		if (CFade::FADE_OUT != CFade::GetFadeMode())
		{
			//決定音
			pSound->PlaySound(CSound::SOUND_LABEL_SE007);
		}
		//フェードが始まったら
		if (CFade::GetFadeMode() != CFade::FADE_OUT)
		{
			CFade::SetFade(CManager::MODE_TITLE);
		}
	}
	//音量の調節
	//pSound->SetVolume(CSound::SOUND_LABEL_SE007, 10.0f);
}
