CMoveUI * CMoveUI::Create(D3DXVECTOR3 pos, D3DXVECTOR3 size, LPDIRECT3DTEXTURE9 pTexture)
{
	CMoveUI * pMoveUI = new CMoveUI;

	pMoveUI->Init();
	pMoveUI->Set(pos, size);
	pMoveUI->BindTexture(pTexture);

	return pMoveUI;
}

