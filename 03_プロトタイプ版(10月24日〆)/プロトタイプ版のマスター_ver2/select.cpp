//*****************************************************************************
// マクロ定義
//*****************************************************************************
#define LOAD_UI		("data\\TEXT\\UI\\Select.txt")

//*****************************************************************************
// 静的メンバ変数宣言
//*****************************************************************************

//=============================================================================
// デフォルトコンストラクタ
//=============================================================================
CSelect::CSelect(){}

//=============================================================================
// デストラクタ
//=============================================================================
CSelect::~CSelect(){}

//=============================================================================
// 初期化処理
//=============================================================================
void CSelect::Init(void)
{
	//ベースの初期化
	CModeBase::Init();

	//UI情報の読み込み
	LoadUI(LOAD_UI);
}

//=============================================================================
// 終了処理
//=============================================================================
void CSelect::Uninit(void)
{
	//ベースの破棄
	CModeBase::Uninit();

	//フェード以外削除
	CScene::ReleaseFade();
}

//=============================================================================
// 更新処理
//=============================================================================
void CSelect::Update(void)
{
	//インプットの取得　
	CGamePad	*pGamePad = CManager::GetInputGamePad();
	CInputKeyboard * pKeyboard = CManager::GetInputkeyboard();

	if (pKeyboard->GetKeyboardTrigger(DIK_RETURN) == true)
	{//タイトルからゲームへ
		if (CFade::GetFadeMode() != CFade::FADE_OUT){ CFade::SetFade(CManager::MODE_TUTORIAL); }
	}
}

//=============================================================================
// 描画処理
//=============================================================================
void CSelect::Draw(void)
{
	CScene::DrawAll();
}