//=============================================================================
// コンストラクタ
//=============================================================================
CLogo::CLogo(int nPriority, CScene::OBJTYPE type) : CScene2D(nPriority, type){}

//=============================================================================
// デストラクタ
//=============================================================================
CLogo::~CLogo(){}

//=============================================================================
// 初期化処理
//=============================================================================
HRESULT CLogo::Init(void)
{
	CScene2D::Init();
	return S_OK;
}

//=============================================================================
// 終了処理
//=============================================================================
void CLogo::Uninit()
{
	CScene2D::Uninit();

	//死亡フラグを立てる
	Release();
}

//=============================================================================
// 更新処理
//=============================================================================
void CLogo::Update(){}

//=============================================================================
// 描画処理
//=============================================================================
void CLogo::Draw()
{

	CGame *pGame = CManager::GetGame();				// ゲームの取得←追加(よしろう)
	CEventCamera *pEveCam = pGame->GetEveCam();		// イベントカメラの取得←追加(よしろう)
	if (pEveCam == NULL && CManager::MODE_GAME == CManager::GetMode())							// イベントカメラが消えていたら←追加(よしろう)
	{
		CScene2D::Draw();
	}
	else if(CManager::MODE_GAME != CManager::GetMode())
	{
		CScene2D::Draw();
	}
}